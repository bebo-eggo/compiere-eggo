package com.audaxis.compiere.eurocenter.ws;

public class AttachFileNotificationReturn {

	/*
	 {
    "application": "smile",
    "operation": "attachFileNotification",
    "success": true,
    "exchangeMessage": "OK",
    "body": true
}
	 */
	
    private String application;
    private String operation;
    private boolean success;
    private String exchangeMessage;
    private boolean body;
 
    public AttachFileNotificationReturn(String application, String operation, boolean success, String exchangeMessage, boolean body) {
        this.application = application;
        this.operation = operation;
        this.success = success;
        this.exchangeMessage = exchangeMessage;
        this.body = body;
    }
 
    @Override
    public String toString() {
        return "AttachFileNotification{" +
                "application='" + application + '\'' +
                ", operation='" + operation + '\'' +
                ", success=" + success +
                ", exchangeMessage=" + exchangeMessage +
                ", body=" + body +
                '}';
    }

	public String getApplication() {
		return application;
	}

	public void setApplication(String application) {
		this.application = application;
	}

	public String getOperation() {
		return operation;
	}

	public void setOperation(String operation) {
		this.operation = operation;
	}

	public boolean isSuccess() {
		return success;
	}

	public void setSuccess(boolean success) {
		this.success = success;
	}

	public String getExchangeMessage() {
		return exchangeMessage;
	}

	public void setExchangeMessage(String exchangeMessage) {
		this.exchangeMessage = exchangeMessage;
	}

	public boolean isBody() {
		return body;
	}

	public void setBody(boolean body) {
		this.body = body;
	}

}
