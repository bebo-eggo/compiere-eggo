CREATE TABLE I_SUBSTITUTE
  (
    "AD_CLIENT_ID" NUMBER(10,0) NOT NULL ENABLE,
    "AD_ORG_ID"    NUMBER(10,0) DEFAULT NULL NOT NULL ENABLE,
    "CREATED" DATE DEFAULT SYSDATE NOT NULL ENABLE,
    "CREATEDBY" NUMBER(10,0) DEFAULT 0 NOT NULL ENABLE,
    "ISACTIVE"  CHAR(1 BYTE) DEFAULT 'Y' NOT NULL ENABLE,
    "UPDATED" DATE DEFAULT SYSDATE NOT NULL ENABLE,
    "UPDATEDBY"       NUMBER(10,0) DEFAULT 0 NOT NULL ENABLE,
    "I_SUBSTITUTE_ID" NUMBER(10,0) DEFAULT NULL NOT NULL ENABLE,
    "M_PRODUCT_ID"    NUMBER(10,0) DEFAULT NULL,
    "SUBSTITUTE_ID"   NUMBER(10,0) DEFAULT NULL,
    "VALUE_M_PRODUCT" NVARCHAR2(50) DEFAULT NULL,
    "VALUE_SUBSTITUTE" NVARCHAR2(50) DEFAULT NULL,
    "NAME" NVARCHAR2(100) DEFAULT NULL,
    "DESCRIPTION" NVARCHAR2(255),
    "I_ERRORMSG" NVARCHAR2(255),
    "PROCESSED"    CHAR(1 BYTE) DEFAULT 'N',
    "PROCESSING"   CHAR(1 BYTE) DEFAULT 'N',
    "I_ISIMPORTED" CHAR(1 BYTE) DEFAULT 'N' NOT NULL ENABLE,
    CHECK (IsActive  IN ('Y','N')) ENABLE,
    CHECK (Processed IN ('Y','N')) ENABLE,
    CONSTRAINT "SUBSTITUTE_KEY" PRIMARY KEY (I_SUBSTITUTE_ID),
    CONSTRAINT "FKCLIENT_SUB" FOREIGN KEY ("AD_CLIENT_ID") REFERENCES AD_CLIENT ("AD_CLIENT_ID") ENABLE,
    CONSTRAINT "FKORG_SUB" FOREIGN KEY ("AD_ORG_ID") REFERENCES AD_ORG ("AD_ORG_ID") ENABLE,
    CONSTRAINT "FKPRODUCT_SUB" FOREIGN KEY ("M_PRODUCT_ID") REFERENCES M_PRODUCT ("M_PRODUCT_ID") ENABLE,
    CONSTRAINT "FKSUB_SUB" FOREIGN KEY ("SUBSTITUTE_ID") REFERENCES M_PRODUCT ("M_PRODUCT_ID") ENABLE
  );